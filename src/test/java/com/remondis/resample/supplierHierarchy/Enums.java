package com.remondis.resample.supplierHierarchy;

import java.util.List;
import java.util.Set;

public class Enums {

  private Enumeration enumValue;
  private List<Enumeration> list;
  private Set<Enumeration> set;

  public Enums() {
    super();
  }

  public Enumeration getEnumValue() {
    return enumValue;
  }

  public void setEnumValue(Enumeration enumValue) {
    this.enumValue = enumValue;
  }

  public List<Enumeration> getList() {
    return list;
  }

  public void setList(List<Enumeration> list) {
    this.list = list;
  }

  public Set<Enumeration> getSet() {
    return set;
  }

  public void setSet(Set<Enumeration> set) {
    this.set = set;
  }

}
